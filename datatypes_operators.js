// Exercise 1: Numbers

// 1. Declare number variables
let integerNum = 10;
let floatNum = 3.5;

// 2. Perform arithmetic operations
let addition = integerNum + floatNum;
let subtraction = integerNum - floatNum;
let multiplication = integerNum * floatNum;
let division = integerNum / floatNum;
let modulus = integerNum % floatNum;
let exponentiation = integerNum ** 2; // raising integerNum to the power of 2

// 3. Print all variables
console.log("Integer:", integerNum);
console.log("Float:", floatNum);
console.log("Addition:", addition);
console.log("Subtraction:", subtraction);
console.log("Multiplication:", multiplication);
console.log("Division:", division);
console.log("Modulus:", modulus);
console.log("Exponentiation:", exponentiation);
