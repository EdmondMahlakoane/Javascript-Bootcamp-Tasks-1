// Exercise 1: Numbers
let intNumber = 10;
let floatNumber = 5.5;

let add = intNumber + floatNumber;
let subtract = intNumber - floatNumber;
let multiply = intNumber * floatNumber;
let divide = intNumber / floatNumber;
let modulus = intNumber % floatNumber;
let exponent = intNumber ** 2;

console.log("Addition:", add);
console.log("Subtraction:", subtract);
console.log("Multiplication:", multiply);
console.log("Division:", divide);
console.log("Modulus:", modulus);
console.log("Exponentiation:", exponent);

// Exercise 2: Boolean and Operators

// Comparison
let comp1 = 10 > 5;
let comp2 = 8 <= 8;
let comp3 = 4 === 4;
let comp4 = 7 !== 10;
console.log("Comparisons:", comp1, comp2, comp3, comp4);

// x and y comparison
let x = 8;
let y = 12;
console.log("x > y:", x > y);
console.log("x <= y:", x <= y);
console.log("x == y:", x == y);
console.log("x != y:", x != y);

// Logical operators
let a = true;
let b = false;
console.log("a && b:", a && b);
console.log("a || b:", a || b);
console.log("!a:", !a);

// Assignment operators
let p = 10;
p += 5;
console.log("p after += 5:", p);
p -= 2;
console.log("p after -= 2:", p);
p *= 3;
console.log("p after *= 3:", p);
p /= 2;
console.log("p after /= 2:", p);
p %= 4;
console.log("p after %= 4:", p);
